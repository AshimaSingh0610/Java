

public class All {

    public static void main(String [] args)
    {
        //! Typecasting 
        /* 
        * Implicit Typecasting
        */
        int a = 10;
        System.out.println(a);

        double b = a;
        System.out.println(a);
        System.out.println(b);

        //! ===============================

        /* 
        * Explicit Typecasting
        */

        int i = 10;
        System.out.println(i);

        double d = (double)i;
        System.out.println(i);
        System.out.println(d);

        //! ===============================

        

    }



    
}
